/**
 * Enqueue block stylesheets.
 */

if ( ! function_exists( 'twentytwentyfour_block_stylesheets' ) ) :
	/**
	 * Enqueue custom block stylesheets
	 *
	 * @since Twenty Twenty-Four 1.0
	 * @return void
	 */
	function twentytwentyfour_block_stylesheets() {
		/**
		 * The wp_enqueue_block_style() function allows us to enqueue a stylesheet
		 * for a specific block. These will only get loaded when the block is rendered
		 * (both in the editor and on the front end), improving performance
		 * and reducing the amount of data requested by visitors.
		 *
		 * See https://make.wordpress.org/core/2021/12/15/using-multiple-stylesheets-per-block/ for more info.
		 */
		wp_enqueue_block_style(
			'core/social-links',
			array(
				'handle' => 'twentytwentyfour-social-links',
				'src'    => get_parent_theme_file_uri( 'assets/css/social-links.css' ),
				'ver'    => wp_get_theme( get_template() )->get( 'Version' ),
				'path'   => get_parent_theme_file_path( 'assets/css/social-links.css' ),
			)
		);		
		wp_enqueue_block_style(
			'core/button',
			array(
				'handle' => 'twentytwentyfour-button-style-outline',
				'src'    => get_parent_theme_file_uri( 'assets/css/button-outline.css' ),
				'ver'    => wp_get_theme( get_template() )->get( 'Version' ),
				'path'   => get_parent_theme_file_path( 'assets/css/button-outline.css' ),
			)
		);		
		wp_enqueue_block_style(
			'core/navigation',
			array(
				'handle' => 'twentytwentyfour-navigation',
				'src'    => get_parent_theme_file_uri( 'assets/css/navigation.css' ),
				'ver'    => wp_get_theme( get_template() )->get( 'Version' ),
				'path'   => get_parent_theme_file_path( 'assets/css/navigation.css' ),
			)
		);
		wp_enqueue_block_style(
			'core/columns',
			array(
				'handle' => 'twentytwentyfour-columns',
				'src'    => get_parent_theme_file_uri( 'assets/css/columns.css' ),
				'ver'    => wp_get_theme( get_template() )->get( 'Version' ),
				'path'   => get_parent_theme_file_path( 'assets/css/columns.css' ),
			)
		);
		wp_enqueue_block_style(
			'core/cover',
			array(
				'handle' => 'twentytwentyfour-cover',
				'src'    => get_parent_theme_file_uri( 'assets/css/cover.css' ),
				'ver'    => wp_get_theme( get_template() )->get( 'Version' ),
				'path'   => get_parent_theme_file_path( 'assets/css/cover.css' ),
			)
		);
		wp_enqueue_block_style(
			'contextual-related-posts/related-posts',
			array(
				'handle' => 'twentytwentyfour-contextual-related-posts-related-posts',
				'src'    => get_parent_theme_file_uri( 'assets/css/contextual-related-posts_related-posts.css' ),
				'ver'    => wp_get_theme( get_template() )->get( 'Version' ),
				'path'   => get_parent_theme_file_path( 'assets/css/contextual-related-posts_related-posts.css' ),
			)
		);
		// wp_enqueue_style( 'my_theme_style', get_stylesheet_uri() );
	}
endif;

add_action( 'init', 'twentytwentyfour_block_stylesheets' );

